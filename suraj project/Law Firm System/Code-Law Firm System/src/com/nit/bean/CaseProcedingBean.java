package com.nit.bean;

public class CaseProcedingBean {
	public String caseno;
	public String casename;
	public String client;
	public String opposiation;
	public String hearingdate;
	public String hearingstage;
	public String hearingdetails;
	public String getCaseno() {
		return caseno;
	}
	public void setCaseno(String caseno) {
		this.caseno = caseno;
	}
	public String getCasename() {
		return casename;
	}
	public void setCasename(String casename) {
		this.casename = casename;
	}
	public String getClient() {
		return client;
	}
	public void setClient(String client) {
		this.client = client;
	}
	public String getOpposiation() {
		return opposiation;
	}
	public void setOpposiation(String opposiation) {
		this.opposiation = opposiation;
	}
	public String getHearingdate() {
		return hearingdate;
	}
	public void setHearingdate(String hearingdate) {
		this.hearingdate = hearingdate;
	}
	public String getHearingstage() {
		return hearingstage;
	}
	public void setHearingstage(String hearingstage) {
		this.hearingstage = hearingstage;
	}
	public String getHearingdetails() {
		return hearingdetails;
	}
	public void setHearingdetails(String hearingdetails) {
		this.hearingdetails = hearingdetails;
	}
	
}
